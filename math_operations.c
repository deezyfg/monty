#include "monty.h"

/**
* f_add - Adds the top two elements of the stack.
* @head: Double pointer to the head of the stack.
* @counter: Line count in the Monty bytecode file.
*
* Return: No return value (void).
*/
void f_add(stack_t **head, unsigned int counter)
{
	stack_t *h;
	int length = 0, temp;

	h = *head;
	while (h)
	{
		h = h->next;
		length++;
	}
	if (length < 2)
	{
		fprintf(stderr, "L%d: can't add, stack too short\n", counter);
		fclose(bus.file);
		free(bus.content);
		free_stack(*head);
		exit(EXIT_FAILURE);
	}
	h = *head;
	temp = h->n + h->next->n;
	h->next->n = temp;
	*head = h->next;
	free(h);
}

/**
* f_sub - Subtracts the top element from
*	the second element in the stack.
* @head: Double pointer to the head of the stack.
* @counter: Line count in the Monty bytecode file.
*
* Return: No return value (void).
*/
void f_sub(stack_t **head, unsigned int counter)
{
	stack_t *temp;
	int sub, nd;

	temp = *head;
	for (nd = 0; temp != NULL; nd++)
		temp = temp->next;
	if (nd < 2)
	{
		fprintf(stderr, "L%d: can't sub, stack too short\n", counter);
		fclose(bus.file);
		free(bus.content);
		free_stack(*head);
		exit(EXIT_FAILURE);
	}
	temp = *head;
	sub = temp->next->n - temp->n;
	temp->next->n = sub;
	*head = temp->next;
	free(temp);
}

